/// Wiki file extensions recognised by the on-disk filtering / search code.
///
/// Keeping the list central (here rather than in `pages` or `search`) means
/// both filesystem traversal and FTS-style code paths stay consistent when
/// extensions are added.
pub(super) const WIKI_EXTENSIONS: &[&str] = &[
    ".md", ".txt", ".py", ".sh", ".bash", ".zsh", ".go", ".rs", ".rb", ".pl",
    ".js", ".ts", ".jsx", ".tsx", ".c", ".cpp", ".h", ".hpp", ".java", ".cs",
    ".swift", ".kt", ".lua", ".r", ".ps1", ".bat", ".cmd", ".php", ".html",
    ".css", ".xml", ".json", ".yaml", ".yml", ".toml", ".ini", ".conf", ".cfg",
    ".sql", ".graphql", ".proto", ".dockerfile", ".nse",
];

/// Predicate: is this filename one of our supported wiki content files?
///
/// Matches by extension (case-insensitive) plus a small set of well-known
/// extensionless filenames (Dockerfile, Makefile, Rakefile).
pub(super) fn is_wiki_file(name: &str) -> bool {
    let lower = name.to_lowercase();
    WIKI_EXTENSIONS.iter().any(|ext| lower.ends_with(ext))
        || lower == "dockerfile"
        || lower == "makefile"
        || lower == "rakefile"
}

/// Predicate: is this filename one whose content we can grep through?
///
/// Currently identical to [`is_wiki_file`] — kept as a separate name so the
/// two intents (storage filter vs. text-search filter) remain
/// distinguishable if they ever diverge.
pub(super) fn is_text_searchable(name: &str) -> bool {
    is_wiki_file(name)
}

/// Resolve the on-disk wiki root: `<app-data>/wiki`.
///
/// Panics if the home directory can't be determined — at this point the
/// app has already started, so this is a configuration bug rather than
/// something to recover from.
pub(super) fn wiki_base_dir() -> PathBuf {
    golish_core::paths::app_data_base()
        .expect("cannot resolve home directory")
        .join("wiki")
}
