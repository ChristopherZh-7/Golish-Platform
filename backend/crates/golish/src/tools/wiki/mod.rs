//! Wiki / vulnerability knowledge-base commands.
//!
//! Originally a single ~1300-line file mixing five distinct concerns; split
//! by feature surface so each domain owns its own file:
//!
//! - [`pages`]       — filesystem-backed page CRUD (`wiki_init`,
//!   `wiki_list`, `wiki_read`, `wiki_write`, `wiki_delete`, `wiki_rename`,
//!   `wiki_create_dir`, `wiki_create_cve`, `wiki_reindex`).  Owns the
//!   YAML-frontmatter parser and the on-disk schema docs.
//! - [`search`]      — `wiki_search` (filesystem grep) and `wiki_search_db`
//!   / `wiki_stats` (Postgres FTS).
//! - [`vuln_links`]  — vulnerability ↔ wiki / PoC / scan history CRUD
//!   (`vuln_link_*` and `vuln_poc_*` commands).
//! - [`kb_research`] — research-log persistence per CVE.
//! - [`dashboard`]   — Karpathy-style wiki overview: page groupings,
//!   changelog, backlinks, orphan pages, full stats.
//!
//! Public command names are unchanged; `lib.rs` references them as
//! `tools::wiki::<name>`, so this module re-exports each command at the
//! crate-visible path.

